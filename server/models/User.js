const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const UserSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    },
    profile: {type: mongoose.Schema.Types.ObjectId, ref: 'Profile'}
});

module.exports = User = mongoose.model("User", UserSchema);
